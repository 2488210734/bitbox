function compare(propertyName) {
	return function(object2, object1) {
		var value1 = object1[propertyName];
		var value2 = object2[propertyName];
		if(value2 < value1) {
			return -1;
		} else if(value2 > value1) {
			return 1;
		} else {
			return 0;
		}
	}
}

